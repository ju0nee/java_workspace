package edu01.ch02;

public class Test06 {
    public static void main(String[] args) {
        // 연산자(operator) : 계산을 위해 약속된 기호
        // 산술 연산자 : 사칙연산 수행
        int a = 10 + 2; // 더하기
        int b = 10 - 2; // 빼기
        int c = 10 * 2; // 곱하기
        int d = 10 / 2; // 나누기
        int e = 10 % 2; // 나머지 값 구하기

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
